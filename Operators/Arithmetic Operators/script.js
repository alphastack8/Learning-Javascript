// Addition (+)
let sum = 10 + 5;
console.log(sum);  // 15

// String concatenation (when one operand is string)
console.log("Hello" + " " + "World");  // "Hello World"
console.log("10" + 5);                 // "105" (string concatenation)
console.log(5 + "10");                 // "510"

// Subtraction (-)
let difference = 10 - 5;
console.log(difference);  // 5

// Multiplication (*)
let product = 10 * 5;
console.log(product);  // 50

// Division (/)
let quotient = 10 / 5;
console.log(quotient);  // 2

// Division with remainder
console.log(10 / 3);    // 3.3333333333333335
console.log(10 / 0);    // Infinity
console.log(-10 / 0);   // -Infinity

// Remainder/Modulo (%)
let remainder = 10 % 3;
console.log(remainder);  // 1

// Real-world modulo examples
console.log(17 % 5);    // 2 (remainder)
console.log(100 % 10);  // 0 (evenly divisible)
console.log(7 % 2);     // 1 (odd number check)

// Exponentiation (**)
let power = 2 ** 3;
console.log(power);  // 8 (2 * 2 * 2)

console.log(5 ** 2);   // 25 (5 squared)
console.log(10 ** 3);  // 1000 (10 cubed)
console.log(4 ** 0.5); // 2 (square root)