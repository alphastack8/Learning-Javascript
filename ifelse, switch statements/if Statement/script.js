// Syntax
if (condition) {
    // code to execute if condition is true
}

// Example 1: Simple check
let age = 18;
if (age >= 18) {
    console.log("You can vote!");
}
// Output: You can vote!

// Example 2: Without curly braces (only one statement)
let score = 85;
if (score > 80) console.log("Great job!");

// Example 3: Multiple statements need braces
let temperature = 30;
if (temperature > 25) {
    console.log("It's hot outside!");
    console.log("Drink water!");
    console.log("Wear sunscreen!");
}